    public class LangManager : ILangMan
    {
        private readonly Dictionary<int, string> fList;

        public LangManager()
        {
            fList = new Dictionary<int, string>();
        }

        public string LS(Enum lsid)
        {
            int idx = ((IConvertible)lsid).ToInt32(null);
            string res;
            return (fList.TryGetValue(idx, out res)) ? res : "?";
        }

        public bool LoadFromFile(string fileName, int offset = 0)
        {
            bool result = false;

            if (File.Exists(fileName))
            {
                fList.Clear();

                using (StreamReader lngFile = new StreamReader(fileName, Encoding.UTF8))
                {
                    bool xt = false;

                    string st;
                    st = lngFile.ReadLine(); // header
                    if (!string.IsNullOrEmpty(st) && st[0] == ';')
                    {
                        st = st.Remove(0, 1);
                        string[] lngParams = st.Split(',');
                        if (lngParams.Length < 3)
                            throw new GKException("Header is incorrect");

                        xt = (lngParams.Length == 4 && lngParams[3] == "xt");
                    }

                    int i = 0 + (offset);
                    while (lngFile.Peek() != -1)
                    {
                        st = lngFile.ReadLine().Trim();

                        if (xt) {
                            // allowed empty and comment strings
                            if (string.IsNullOrEmpty(st) || st[0] == ';') {
                                continue;
                            }

                            string[] parts = st.Split('=');
                            i = int.Parse(parts[0]);
                            fList.Add(i, parts[1]);
                        } else {
                            fList.Add(i, st);
                            i++;
                        }
                    }
                    result = true;
                }
            }

            return result;
        }

        internal void DefInit(string[] source)
        {
            fList.Clear();

            for (LSID id = LSID.LSID_First; id <= LSID.LSID_Last; id++)
            {
                int idx = (int)id;
                fList.Add(idx, source[idx - 1]);
            }
        }
    }
